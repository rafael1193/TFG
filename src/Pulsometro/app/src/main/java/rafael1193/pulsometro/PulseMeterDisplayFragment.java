/**
 * This fragment controls Bluetooth to communicate with other devices.
 */
public class PulseMeterDisplayFragment extends Fragment {

    private static final String TAG = "BluetoothDebuggerFragment";
    private static final int HISTORY_SIZE = 10;

    // Intent request codes
    private static final int REQUEST_CONNECT_DEVICE_SECURE = 1;
    private static final int REQUEST_ENABLE_BT = 3;

    // Layout Views
    private TextView mBeatsTextView;
    private XYPlot mHistoryPlot = null;
    private SimpleXYSeries mBeatsHistorySeries = null;

    /**
     * Name of the connected device
     */
    private String mConnectedDeviceName = null;

    /**
     * Local Bluetooth adapter
     */
    private BluetoothAdapter mBluetoothAdapter = null;

    /**
     * Member object for the chat services
     */
    private BluetoothChatService mChatService = null;

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setHasOptionsMenu(true);
        // Get local Bluetooth adapter
        mBluetoothAdapter = BluetoothAdapter.getDefaultAdapter();

        // If the adapter is null, then Bluetooth is not supported
        if (mBluetoothAdapter == null) {
            FragmentActivity activity = getActivity();
            Toast.makeText(activity, "Bluetooth is not available", Toast.LENGTH_LONG).show();
            activity.finish();
        }
    }


    @Override
    public void onStart() {
        super.onStart();
        // If BT is not on, request that it be enabled.
        // setupChat() will then be called during onActivityResult
        if (!mBluetoothAdapter.isEnabled()) {
            Intent enableIntent = new Intent(BluetoothAdapter.ACTION_REQUEST_ENABLE);
            startActivityForResult(enableIntent, REQUEST_ENABLE_BT);
            // Otherwise, setup the chat session
        } else if (mChatService == null) {
            setupChat();
        }
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        if (mChatService != null) {
            mChatService.stop();
        }
    }

    @Override
    public void onResume() {
        super.onResume();

        // Performing this check in onResume() covers the case in which BT was
        // not enabled during onStart(), so we were paused to enable it...
        // onResume() will be called when ACTION_REQUEST_ENABLE activity returns.
        if (mChatService != null) {
            // Only if the state is STATE_NONE, do we know that we haven't started already
            if (mChatService.getState() == BluetoothChatService.STATE_NONE) {
                // Start the Bluetooth chat services
                mChatService.start();
            }
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container,
                             @Nullable Bundle savedInstanceState) {
        return inflater.inflate(R.layout.fragment_bluetooth_chat, container, false);
    }

    @Override
    public void onViewCreated(View view, @Nullable Bundle savedInstanceState) {
        mBeatsTextView = (TextView) view.findViewById(R.id.beatsTextView);
        mBeatsTextView.setText(getString(R.string.beats_per_minute, "‒‒"));

        final PlotStatistics histStats = new PlotStatistics(1000, false);

        mHistoryPlot = (XYPlot) view.findViewById(R.id.historyPlot);
        mBeatsHistorySeries = new SimpleXYSeries(getString(R.string.beats));
        mBeatsHistorySeries.useImplicitXVals();
        mHistoryPlot.setRangeBoundaries(40, 200, BoundaryMode.FIXED);
        mHistoryPlot.setDomainBoundaries(0, HISTORY_SIZE, BoundaryMode.FIXED);
        mHistoryPlot.addSeries(mBeatsHistorySeries, new LineAndPointFormatter());
        mHistoryPlot.setDomainStepValue(6);
        mHistoryPlot.setRangeStepValue(9);
        mHistoryPlot.setTicksPerRangeLabel(1);
        mHistoryPlot.setDomainLabel(getString(R.string.time));
        mHistoryPlot.getDomainLabelWidget().pack();
        mHistoryPlot.setRangeLabel(getString(R.string.beats));
        mHistoryPlot.getRangeLabelWidget().pack();
        mHistoryPlot.addListener(histStats);

        //Adjust sizes
        mHistoryPlot.getGraphWidget().getDomainLabelPaint().setTextSize(25.0f);
        mHistoryPlot.getGraphWidget().getRangeLabelPaint().setTextSize(25.0f);
        mHistoryPlot.getGraphWidget().setMarginBottom(30.0f);
        mHistoryPlot.getGraphWidget().setMarginLeft(30.0f);
        mHistoryPlot.getGraphWidget().setMarginRight(30.0f);
        mHistoryPlot.getGraphWidget().setMarginTop(30.0f);
        mHistoryPlot.getRangeLabelWidget().getLabelPaint().setTextSize(20.0f);
        //mHistoryPlot.getRangeLabelWidget().setWidth(50.0f);
        mHistoryPlot.getRangeLabelWidget().setMarginRight(50.0f);
        mHistoryPlot.getDomainLabelWidget().setVisible(false);

        //mHistoryPlot.getGraphWidget().setDomainLabelWidth(20.0f);
        mHistoryPlot.getLegendWidget().setVisible(false);
    }

    /**
     * Set up the UI and background operations for chat.
     */
    private void setupChat() {
        Log.d(TAG, "setupChat()");

        // Initialize the BluetoothChatService to perform bluetooth connections
        mChatService = new BluetoothChatService(getActivity(), mHandler);
    }

    /**
     * Updates the status on the action bar.
     *
     * @param resId a string resource ID
     */
    private void setStatus(int resId) {
        FragmentActivity activity = getActivity();
        if (null == activity) {
            return;
        }
        final ActionBar actionBar = activity.getActionBar();
        if (null == actionBar) {
            return;
        }
        actionBar.setSubtitle(resId);
    }

    /**
     * Updates the status on the action bar.
     *
     * @param subTitle status
     */
    private void setStatus(CharSequence subTitle) {
        FragmentActivity activity = getActivity();
        if (null == activity) {
            return;
        }
        final ActionBar actionBar = activity.getActionBar();
        if (null == actionBar) {
            return;
        }
        actionBar.setSubtitle(subTitle);
    }

    public void updatePlot(Integer value) {

        // get rid the oldest sample in history:
        if (mBeatsHistorySeries.size() > HISTORY_SIZE) {
            mBeatsHistorySeries.removeFirst();
        }

        // add the latest history sample:
        mBeatsHistorySeries.addLast(null, value);

        // redraw the Plots:
        mHistoryPlot.redraw();
    }

    /**
     * The Handler that gets information back from the BluetoothChatService
     */
    private final Handler mHandler = new Handler() {
        @Override
        public void handleMessage(Message msg) {
            FragmentActivity activity = getActivity();
            switch (msg.what) {
                case Constants.MESSAGE_STATE_CHANGE:
                    switch (msg.arg1) {
                        case BluetoothChatService.STATE_CONNECTED:
                            setStatus(getString(R.string.title_connected_to, mConnectedDeviceName));
                            break;
                        case BluetoothChatService.STATE_CONNECTING:
                            setStatus(R.string.title_connecting);
                            break;
                        case BluetoothChatService.STATE_LISTEN:
                        case BluetoothChatService.STATE_NONE:
                            setStatus(R.string.title_not_connected);
                            break;
                    }
                    break;
                case Constants.MESSAGE_READ:
                    byte[] readBuf = (byte[]) msg.obj;

                    Integer firstValue = (int)readBuf[0];
                    // This hack is needed because java doesn't have unsigned numbers...
                    if(readBuf[0] < 0)
                    {
                        firstValue= (int) readBuf[0] & 0xff;
                    }

                    Log.d(TAG, "Recv(unsigned): " + firstValue.toString());
                    if (firstValue == 0){
                        mBeatsTextView.setText(getString(R.string.beats_per_minute, "‒‒"));
                    }else {
                        mBeatsTextView.setText(getString(R.string.beats_per_minute, firstValue.toString()));
                    }

                    updatePlot(firstValue);
                    break;
                case Constants.MESSAGE_DEVICE_NAME:
                    // save the connected device's name
                    mConnectedDeviceName = msg.getData().getString(Constants.DEVICE_NAME);
                    if (null != activity) {
                        Toast.makeText(activity, "Connected to "
                                + mConnectedDeviceName, Toast.LENGTH_SHORT).show();
                    }
                    break;
                case Constants.MESSAGE_TOAST:
                    if (null != activity) {
                        Toast.makeText(activity, msg.getData().getString(Constants.TOAST),
                                Toast.LENGTH_SHORT).show();
                    }
                    break;
            }
        }
    };

    public void onActivityResult(int requestCode, int resultCode, Intent data) {
        switch (requestCode) {
            case REQUEST_CONNECT_DEVICE_SECURE:
                // When DeviceListActivity returns with a device to connect
                if (resultCode == Activity.RESULT_OK) {
                    connectDevice(data, true);
                }
                break;
            case REQUEST_ENABLE_BT:
                // When the request to enable Bluetooth returns
                if (resultCode == Activity.RESULT_OK) {
                    // Bluetooth is now enabled, so set up a chat session
                    setupChat();
                } else {
                    // User did not enable Bluetooth or an error occurred
                    Log.d(TAG, "BT not enabled");
                    Toast.makeText(getActivity(), R.string.bt_not_enabled_leaving,
                            Toast.LENGTH_SHORT).show();
                    getActivity().finish();
                }
        }
    }

    /**
     * Establish connection with other divice
     *
     * @param data   An {@link Intent} with {@link DeviceListActivity#EXTRA_DEVICE_ADDRESS} extra.
     * @param secure Socket Security type - Secure (true) , Insecure (false)
     */
    private void connectDevice(Intent data, boolean secure) {
        // Get the device MAC address
        String address = data.getExtras()
                .getString(DeviceListActivity.EXTRA_DEVICE_ADDRESS);
        // Get the BluetoothDevice object
        BluetoothDevice device = mBluetoothAdapter.getRemoteDevice(address);
        // Attempt to connect to the device
        mChatService.connect(device, secure);
    }

    @Override
    public void onCreateOptionsMenu(Menu menu, MenuInflater inflater) {
        inflater.inflate(R.menu.bluetooth_chat, menu);
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        switch (item.getItemId()) {
            case R.id.secure_connect_scan: {
                // Launch the DeviceListActivity to see devices and do scan
                Intent serverIntent = new Intent(getActivity(), DeviceListActivity.class);
                startActivityForResult(serverIntent, REQUEST_CONNECT_DEVICE_SECURE);
                return true;
            }
        }
        return false;
    }

}
